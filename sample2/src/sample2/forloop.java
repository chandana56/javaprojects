package sample2;
class forloop {
	public static void main(String args[])
	{
		 int[] x = new int[] {10,20,30,40,50};
		 //for(initialization; expression; increment)//
		int y = 0;
		for(y=0; y<=4; y++) {
			System.out.println(x[y]);
		}
		System.out.println("..............");
	int i = 0;
	for(i=0; i<=5; i++) 
	{
	System.out.println(i);	
	}
	}
	}

 